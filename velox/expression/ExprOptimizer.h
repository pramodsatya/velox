#include "velox/core/Expressions.h"
#include "velox/core/QueryCtx.h"

namespace facebook::velox::expression {

/// Optimizes expression through a combination of constant folding and rewrites;
/// all possible subtrees of the expression are constant folded first in a
/// bottom up manner, then the folded expression is rewritten. When the argument
/// 'replaceEvalErrorWithFailExpr' is 'true':
///  1. If an exception (i.e VeloxUserError) is encountered during optimization
///     of any subtree of the expression, the failing subexpression is replaced
///     with a fail expression containing the error message.
///  2. Users should be mindful of this change, since the optimized expression
///     might not be executable in Velox without adding the necessary type casts
///     as expected by the registered functions.
/// When 'replaceEvalErrorWithFailExpr' is 'false', any exception encountered
/// while optimizing the expression is ignored and the expression is left
/// unchanged.
core::TypedExprPtr optimize(
    const core::TypedExprPtr& expr,
    core::QueryCtx* queryCtx,
    memory::MemoryPool* pool,
    bool replaceEvalErrorWithFailExpr);
} // namespace facebook::velox::expression
